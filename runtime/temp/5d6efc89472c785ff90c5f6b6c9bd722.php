<script>

  //商品属性的添加或者多个添加    //上传图片或者多个图片
  function addtr(obj) {
    //获取一个a标签的onclick事件.
    //获取到$(obj).parent().parent()子元素
    var tr = $(obj).parent().parent();
    //判断对象下面的值是不是+如果是就上一个div,不是就减去一个
    if ($(obj).html() == '+') {
      //把上面的值从新克隆一份给divnew
      var divnew = tr.clone();
      divnew.find('a').html('-');
      tr.after(divnew);
    }else {
      tr.remove();
    }
  }

